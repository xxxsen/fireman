// System profile identities form an append-only, immutable chain. Each published
// identity's canonical content is frozen forever; correcting the model or its
// evidence must publish a NEW identity rather than editing a released one in place
// (td/064 R6, td/066 R12). The history:
//
//   - system_cma_v1@1 (td/061/062): original CNY-only seed priors.
//   - system_cma_v2@1 (td/064): added native USD/HKD priors + tail truncation, but
//     its nominal returns used an additive (non-geometric) shortcut (td/066 R11).
//   - system_cma_v3@1 (td/066): same structure, but every return/FX prior is the
//     EXACT compounded value built from the committed CMA evidence artifact.
//
// v1 and v2 are never generated by code anymore: they only live in already-upgraded
// databases (for replay and explicit pins). EnsureSystemDefault publishes and
// migrates to the current identity (v3) only.
const (
	// SystemProfileID / SystemProfileVersion are the CURRENT default system
	// identity. Code that resolves "the system default" uses these.
	SystemProfileID      = "system_cma_v3"
	SystemProfileVersion = 1

	// SystemProfileV2ID / V2Version is the frozen td/064 identity (direct
	// predecessor of v3).
	SystemProfileV2ID      = "system_cma_v2"
	SystemProfileV2Version = 1

	// SystemLegacyProfileID / Version is the original td/061/062 identity. It is
	// never updated or deleted so old runs and v1 pins replay exactly.
	SystemLegacyProfileID      = "system_cma_v1"
	SystemLegacyProfileVersion = 1
)

// SystemProfileIDPrefix is the reserved id namespace for system-owned profiles.
// A user profile may NEVER use an id with this prefix, otherwise it could shadow a
// system identity in the (id, version)-keyed table and steal its CMA evidence
// provenance (td/067 R13).
const SystemProfileIDPrefix = "system_cma_"

// HasReservedSystemID reports whether an id is in the reserved system namespace.
func HasReservedSystemID(id string) bool {
	return strings.HasPrefix(id, SystemProfileIDPrefix)
}

