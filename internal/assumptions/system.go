// SystemProfileIdentity is one immutable entry in the system-profile registry.
type SystemProfileIdentity struct {
	ID            string
	Version       int
	CanonicalHash string // frozen SHA-256 of the canonical JSON
	EvidenceHash  string // SHA-256 of the backing evidence artifact ("" if none)
	Predecessor   string // "id@version" of the direct predecessor ("" for the first)
}

// Ref returns the "id@version" reference for this identity.
func (e SystemProfileIdentity) Ref() string {
	return profileRef(e.ID, e.Version)
}

var systemProfileRegistry = []SystemProfileIdentity{
	{ID: SystemLegacyProfileID, Version: SystemLegacyProfileVersion, CanonicalHash: systemProfileV1CanonicalHash},
	{
		ID: SystemProfileV2ID, Version: SystemProfileV2Version, CanonicalHash: systemProfileV2CanonicalHash,
		Predecessor: profileRef(SystemLegacyProfileID, SystemLegacyProfileVersion),
	},
	{
		ID: SystemProfileID, Version: SystemProfileVersion,
		CanonicalHash: systemProfileV3CanonicalHash, EvidenceHash: systemProfileV3EvidenceHash,
		Predecessor: profileRef(SystemProfileV2ID, SystemProfileV2Version),
	},
}

// SystemProfileRegistry returns the immutable system-profile identity chain.
func SystemProfileRegistry() []SystemProfileIdentity {
	out := make([]SystemProfileIdentity, len(systemProfileRegistry))
	copy(out, systemProfileRegistry)
	return out
}

// CurrentSystemIdentity is the registry entry for the current default identity.
func CurrentSystemIdentity() SystemProfileIdentity {
	return systemProfileRegistry[len(systemProfileRegistry)-1]
}

// LookupSystemIdentity returns the registry entry for an id@version, if any.
func LookupSystemIdentity(id string, version int) (SystemProfileIdentity, bool) {
	for _, e := range systemProfileRegistry {
		if e.ID == id && e.Version == version {
			return e, true
		}
	}
	return SystemProfileIdentity{}, false
}

// IsCurrentSystemDefaultIdentity reports whether id@version is the current
// default-able system identity (v3). A system-owned profile that is not the
// current identity (v1/v2) can be used for replay or an explicit pin but can never
// be (re)selected as the global default (td/066 R12).
func IsCurrentSystemDefaultIdentity(id string, version int) bool {
	cur := CurrentSystemIdentity()
	return id == cur.ID && version == cur.Version
}

// CurrentSystemPredecessorRef is the "id@version" of the direct predecessor of the
// current system identity (v2), the only system default the upgrade auto-migrates
// from.
func CurrentSystemPredecessorRef() string {
	return CurrentSystemIdentity().Predecessor
}

// HistoricalSystemProfileVariant is one recognized, immutable published system
// profile CONTENT, keyed by (id, version, content_hash). Unlike the identity chain
// (one canonical content per identity), the variant registry also records contents
// that were briefly published under an already-used identity — notably the TD 065
// v2 variant (td/067 R14). It is read-only: it backs historical replay/pin
// provenance and lets EnsureSystemDefault recognize a legitimate historical system
// row, but a variant can never become the global default.
type HistoricalSystemProfileVariant struct {
	ID            string
	Version       int
	CanonicalHash string
	EvidenceHash  string // backing CMA evidence artifact hash ("" if none)
	Note          string
}

// Ref returns the "id@version" reference for this variant.
func (v HistoricalSystemProfileVariant) Ref() string {
	return profileRef(v.ID, v.Version)
}

